                    <h4 class="display-6 mb-3 text-center">Antiragging Committee</h4> <hr class="text-primary">
                    <p align="justify">We at CTCP, Shahpur, are delighted to be a ragging-free campus and we have zero tolerance for it. In order to maintain a campus free of discrimination, students are educated to follow the act. A committee against ragging has been established to prohibit, cease, and ultimately get eliminate of the vile act of ragging. The Anti-ragging Committee is nominated and directed by the Principal and consists of representatives as prescribed by the UGC Regulations, including civil and police administration, local media, non-governmental organizations involved in youth activities, and representatives from the institute. The committee's objective is to ensure adherence to the UGC Regulations on preventing the problem of ragging in higher educational institutions, 2009.</p>
                    <p align="justify">A.	The committee will supervise the Anti-Ragging Squad's efforts to eliminate ragging on campus and make sure that the anti-ragging policies are adhered to. As required by Section 26(1)(g) of the University Grants Commission Act, 1956, CTCP, Shahpur complies strictly with the government's anti-ragging policy and the guidelines established outward by the UGC, which were notified vide no.F.1-16/2009 (CPP-II) dated 21-10-2009 on preventing the risk of Ragging in Higher Educational Institutions, 2009 (Under Section 26(1) (g) of the University Grants Commission Act, 1956). Any one or more of the following actions that have the potential to harm a new or younger student physically, emotionally, socially, intellectually, or psychologically, or to create fear or anxiety about this form of harm, are classified as ragging by the Constitutional Supreme Court of India. It includes</p>
                    <ol>
                        <li>Any behavior on the part of a student body, either individually or collectively, that can be described as verbal, written, or nonverbal, as well as actions that have the potential to intimidate, threaten, or tease a new student or any other student, or to treat them rudely.</li>
                        <li>Indulging in distracting or indiscipline actions by any student or students which causes or is likely to cause discomfort, hardship, physical, emotional or psychic harm or to induce fear or apprehension thereof in any fresher or any other student.</li>
                        <li>Asking a student to perform an act that they would not perform in a regular class and that will cause them to feel ashamed, tormented, or embarrassed in order to negatively impact their physical or mental health, as well as the physical health of other students, is considered bullying.</li>
                        <li>Any action taken by a senior student that hinders, interferes with, or interrupts the first-year or other student's normal academic day.</li>
                        <li>Taking advantage of a new student or any other student to finish the assignments given to a person or group of people.</li>
                        <li>Any kind of forced spending or financial exploitation committed by students against the newcomer or any other student.</li>
                        <li>Any physical abuse act, including all of its alternatives, such as sexual abuse, gay assaults, stripping, coercing someone into performing obscene or indecent gestures, inflicting bodily harm, or posing a risk to their health or well-being.</li>
                        <li>Any act of abuse, whether physical or by written messages, email messages, posts, or public insults, that also includes getting a perverse sense of pleasure or a sadistic thrill by actively or passively taking part in the humiliation of new students or any other student.</li>
                        <li>Any behavior that degrades a new student's mental well-being and confidence, whether or not it is done with the intention of causing psychopathic satisfaction, or any act in which a student displays their influence, authority, or superiority over any other student or newcomers</li>
                    </ol>
                    <p align="justify">B. The National Anti-Ragging Helpline 1800-180-5522 (Toll Free), the website www.antiragging.in, the email info@amanmovement.org, and the mobile numbers 09871170303, 09818400116 (only in case of emergency) can be used by students or parents who are experiencing discomfort due to ragging in an emergency.</p>
                    <p align="justify">The College strictly prohibits ragging, and anyone who participate in or contributes to ragging may face repercussions as it is a crime.</p>

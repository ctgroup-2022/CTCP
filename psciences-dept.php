    <h3 class="text-center">About Department    </h3><hr>
    <p align="justify"> The Department of Pharmaceutical Sciences offers the Pharm D and Pharm D(PB) programs in addition to the M. Pharmacy (Pharmaceutical Chemistry, Pharmaceutics, Pharmacology).
The M. Pharm program provides innovative training, advanced learning and aims to produce proficient and capable technologists, academicians, pre-clinical and clinical scientists, clinical pharmacists, corporate executives, and entrepreneurs in the pharmaceutical industry. Pharm. D is a 6 year doctoral program which includes 5 years of study and 1 year internship. The course mainly guides the students to help the patient with drug information, and to make them understand the importance of therapy given, and also to help the Physicians in choosing the right drug to the patient.
</p>

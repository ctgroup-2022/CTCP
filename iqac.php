    <h3 class="text-center">About IQAC</h3><hr>
    <p class="text-justify"> The Internal Quality Assurance Cell, often known as IQAC, is a structure that helps higher education institutions to achieve their learning outcomes and objectives while also optimizing their operations.</p>
      <p class="text-justify"> Establishing an Internal Quality Assurance Cell (IQAC) in accordance with National Assessment and Accreditation Council (NAAC) guidelines is a recommended accreditation quality management strategy for higher education institutions. Since maintaining and improving quality is an ongoing endeavor, the IQAC endeavors to accomplish these aims by integrating into the institution's framework. </p>
      <p class="text-justify"> The primary duty of the IQAC is to provide a framework that will systematically, reliably, and catalytically enhance the overall performance of institutions. Through the establishment of best practices and the cultivation of a quality-conscious culture, IQAC aims to encourage actions that will increase institutions' efficiency. </p>
      <h3><strong>Objectives</strong></h3>
      <p class="text-justify">IQAC is established up to fulfill the following objectives:</p>
      <p><ul>
        <li>To provide a solid framework that improves the administrative and academic performance of the    institution .</li>
        <li>To promote and advance a culture that makes use of human resources and best practices while aiming for constant improvement of quality in all facets..</li>
        <li>To ensure that the institute is managed with concentration, clarity, and transparency in every aspect. </li>
      </ul></p>
      <h3><strong>Strategies</strong></h3>
      <p class="text-justify">IQAC will be instrumental in establishing methods, policies and procedures for:</p>
      <p><ul>
        <li>Ensuring that all administrative and academic duties are carried out efficiently, on schedule, and with proper planning.</li>

        <li>Ensuring that academic programs fulfill the necessary requirements and are applicable in the manufacturing field.</li>

        <li>Utilizing novel teaching-learning strategies and formats.</li>

        <li>Ensuring that the services and support network that are required are provided.</li>

        <li>Applying into practice effective assessment criteria.</li>
      </ul></p>
  </div>

